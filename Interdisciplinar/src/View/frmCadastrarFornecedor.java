    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(txtId.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null ,"O Campo nome precisa ser preenchido");
            
        }
        else
        {
            
         VariaveisGlobais id = new VariaveisGlobais();
        CriaTabelaFornecedores pesquisar = new CriaTabelaFornecedores();
       
        
        
        ///Objetos para salvar
        Fornecedor forn = new Fornecedor();
        forn.setNome(txtNome.getText());
        forn.setId(Integer.valueOf(txtId.getText()));
        forn.setEndereco(txtEndereco.getText());
        forn.setCNPJ(txtCNPJ.getText());
        forn.setBairro(txtBairro.getText());
        forn.setCidade(txtCidade.getText());
        forn.setTelefone(txtTelefone.getText());
        forn.setUf(cmbUF.getSelectedItem().toString());
        forn.setCep(txtCEP.getText());
        forn.setEmail(txtEmail.getText());
        
        this.txtNome.setEnabled(false);
        this.txtTelefone.setEnabled(false);
        this.txtBairro.setEnabled(false);
        this.txtCEP.setEnabled(false);
        this.txtCNPJ.setEnabled(false);
        this.txtEndereco.setEnabled(false);
        this.txtEmail.setEnabled(false);
        this.txtCidade.setEnabled(false);
        this.cmbUF.setEnabled(false);
      
        CriaTabelaFornecedores cadastrar = new CriaTabelaFornecedores();
        try {
            
           connect();
            try {         
            VariaveisGlobais VG = new VariaveisGlobais();
            //verifica se o cadastro existe pelo id
            stat = c.createStatement();
            op = "SELECT id FROM fornecedores WHERE id='" +txtId.getText()+"'";
            System.out.println(op);  
            ResultSet rs = stat.executeQuery(op);
            if(rs.next())
            {    //se existir so da update
                stat.close();
                cadastrar.AltFornecedores(forn);
            }
            else
            {   //se não existir ele insere
                stat.close();
                cadastrar.cadFornecedor(forn);
            }
             
            } catch (SQLException e) {
            
            }           
            
            
            txtNome.setText("");
            txtId.setText("");
            txtEndereco.setText("");            
            txtBairro.setText("");
            txtCidade.setText("");
            txtCEP.setText(""); 
            txtEmail.setText("");
            txtCNPJ.setText("");
            txtTelefone.setText("");
            txtNome.setFocusable(true);         
             
       
       
            
        } catch (Exception ex) {
            Logger.getLogger(frmCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        btnSalvar.setEnabled(false);
        btnIncluir.setEnabled(true);
    }//GEN-LAST:event_btnSalvarActionPerformed

