    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        if(txtId.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null ,"O campo id esta vazio");
            return;
        }
        else
        {
            int resposta;
            resposta = JOptionPane.showConfirmDialog(null, "Deseja deleta o produto?");
            if(resposta == JOptionPane.YES_OPTION )
            {
                CriaTabelaFornecedores deleta = new CriaTabelaFornecedores();
                try {
                    String id = txtId.getText();
                    deleta.DeleteFornecedoresID(id);
                    txtId.setText("");
                    txtBairro.setText("");
                    txtCEP.setText("");
                    txtCNPJ.setText("");
                    txtCidade.setText("");
                    txtEmail.setText("");
                    txtEndereco.setText("");
                    txtNome.setText("");
                    txtTelefone.setText("");
                } catch (SQLException ex) {
                    Logger.getLogger(frmCadastrarFornecedor.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

